ll solve_r(ll L, ll R)
{
	if (L == R || R == 0)
		return 1;

	ll& ret = DP2[L][R];

	if (ret != -1)
		return ret;

	ret = solve_r(L - 1, R - 1);

	add_mod(ret, solve_r(L - 1, R));

	return ret;
}

