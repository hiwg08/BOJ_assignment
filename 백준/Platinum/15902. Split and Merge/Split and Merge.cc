{
	ll LQ = a + b - 1, RQ = b;

	return solve_r(LQ, RQ);
}

void precomp()
{
	ll hi = 0, lo = 0, cnt = 0;

	ll sum_hi = U[0], sum_lo = D[0];

	while (true)
	{
		if (sum_hi == sum_lo)
		{
			if (cnt != 0) // 반드시 잘라야 하는 애들
			{
				mul_mod(pas, DP[cnt]);

				mul_mod(pas, get_fac(total + 1, cnt));

				// 딱 여기까지 추가된 막대기는 total

				total += cnt;
			}

			cnt = 0;

			lo++, hi++;

			if (hi < (ll)U.size())
				sum_hi = U[hi];
			if (lo < (ll)D.size())
				sum_lo = D[lo];
		}
		else if (sum_hi < sum_lo)
		{
			hi++;
			sum_hi += U[hi];
			cnt++;
		}
		else
		{
			lo++;
			sum_lo += D[lo];
			cnt++;
		}

		if (hi >= (ll)U.size() && lo >= (ll)D.size())
			break;
	}
}

